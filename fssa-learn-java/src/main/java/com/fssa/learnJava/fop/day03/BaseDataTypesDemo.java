package com.fssa.learnJava.fop.day03;

public class BaseDataTypesDemo {

	public static void main(String[] args) {
		
		boolean flag = true; //initialization
		boolean verbose, debug; // two variables declared, but not yet initialized
		char grade = 'A';
		byte b = 12;
		short s = 24;
		int i, j; // three variables declared; only k initialized
		long l = 890l; // note the use of ”L” here
		float pi = 3.1416f; // note the use of ”F” here
		double e = 2.71828, a = 6.022e23; // both variables are initialized
		double num1=6.0222e10;
		double num2=0.000000006022;
		System.out.println(num1);
		System.out.println(num2);
	}

}
